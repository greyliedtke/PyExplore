# character class to send to led matrix
class Char_Setting:
    def __init__(self, offset):
        self.offset = offset
        self.character = 0

    def char_matrix(self, character):
        char_matrix = chars.char_dict[character]
        char_x, char_y = [], []

        for i, row in enumerate(char_matrix):
            for j, b in enumerate(row):
                if b == 1:
                    char_x.append(self.offset[0] + j)
                    char_y.append(self.offset[1] + (4-i))

        return [char_x, char_y]

characters = [Char_Setting(cs) for cs in chars.char_offset]

